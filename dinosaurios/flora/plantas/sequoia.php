<h2>¿Qué significa su nombre?</h2>
<p>El nombre <strong>Sequoia</strong> fue otorgado en honor al líder nativo estadounidense <strong>Sequoyah</strong>, quien desarrolló un sistema de escritura para el pueblo cherokee. Se utiliza para referirse a un género de árboles gigantes en la familia Cupressaceae.</p>

<h2>Clasificación científica</h2>
<ul>
    <li><strong>Reino:</strong> Plantae</li>
    <li><strong>División:</strong> Coniferophyta</li>
    <li><strong>Clase:</strong> Pinopsida</li>
    <li><strong>Orden:</strong> Pinales</li>
    <li><strong>Familia:</strong> Cupressaceae</li>
    <li><strong>Género:</strong> Sequoia</li>
    <li><strong>Especies:</strong> Sequoia sempervirens (Secuoya costera), Sequoia gigantea (Secuoya gigante).</li>
</ul>

<h2>Periodo geológico</h2>
<p>Las Sequoias existen desde hace más de <strong>150 millones de años</strong>, en el Mesozoico, y continúan siendo algunas de las especies más antiguas y gigantes de la Tierra.</p>

<h2>Distribución geográfica</h2>
<p>Las secuoyas se encuentran principalmente en la costa de California, Estados Unidos, donde crecen en regiones templadas y montañosas. La Sequoia gigantea se encuentra en las montañas de Sierra Nevada.</p>

<h2>Características físicas</h2>
<ul>
    <li><strong>Altura:</strong> Pueden alcanzar alturas impresionantes, con la Sequoia sempervirens superando los 100 metros.</li>
    <li><strong>Tronco:</strong> De gran diámetro, con corteza gruesa, rugosa y rojiza.</li>
    <li><strong>Hojas:</strong> Aciculares, en forma de aguja, dispuestas en espiral y de color verde oscuro.</li>
    <li><strong>Vida:</strong> Las secuoyas son árboles longevos, pudiendo vivir más de 3,000 años.</li>
</ul>

<h2>Alimentación</h2>
<p>Al igual que todas las plantas, las secuoyas realizan <strong>fotosíntesis</strong> para producir su propio alimento utilizando luz solar, agua y dióxido de carbono.</p>

<h2>Comportamiento</h2>
<p>Las secuoyas son plantas perennes, muy resistentes a enfermedades y plagas. Su crecimiento es extremadamente lento, pero continuo, lo que les permite alcanzar tamaños gigantescos a lo largo de los siglos.</p>

<h2>Reproducción</h2>
<p>La reproducción de la secuoya es sexual. Los conos masculinos liberan polen que es transportado por el viento hacia los conos femeninos, donde se desarrollan las semillas, que caen al suelo para germinar.</p>

<h2>Descubrimiento</h2>
<p>Las Sequoias fueron descritas científicamente en el siglo XIX. Su enorme tamaño y longevidad las han convertido en un símbolo de la naturaleza estadounidense, especialmente en los parques nacionales de California.</p>

<h2>Relación con otras plantas</h2>
<p>Las Sequoias pertenecen a la familia Cupressaceae, que también incluye otras coníferas como los cipreses y los enebros. Están estrechamente relacionadas con otras especies de árboles gigantes como el pino de California.</p>

<h2>Curiosidades</h2>
<ul>
    <li>La Sequoia sempervirens es el árbol más alto del mundo, conocido como el "General Sherman", que se encuentra en el Parque Nacional de las Secuoyas.</li>
    <li>Las Sequoias pueden vivir más de 3,000 años, lo que las convierte en algunos de los seres vivos más longevos de la Tierra.</li>
    <li>La corteza de la Sequoia es resistente al fuego, lo que ayuda a proteger al árbol de los incendios forestales.</li>
    <li>El tronco de las secuoyas es tan grande que algunos árboles huecos pueden albergar hasta varias personas en su interior.</li>
</ul>

