<h2>¿Qué significa su nombre?</h2>
<p><strong>Tyrannosaurus rex</strong> proviene del griego y el latín: "tyrannos" (tirano), "sauros" (lagarto) y "rex" (rey), lo que se traduce como "Rey Lagarto Tirano". Fue nombrado por el paleontólogo Henry Fairfield Osborn en 1905.</p>

<h2>Clasificación científica</h2>
<ul>
    <li><strong>Reino:</strong> Animalia</li>
    <li><strong>Filo:</strong> Chordata</li>
    <li><strong>Clase:</strong> Reptilia</li>
    <li><strong>Orden:</strong> Saurischia</li>
    <li><strong>Suborden:</strong> Theropoda</li>
    <li><strong>Familia:</strong> Tyrannosauridae</li>
    <li><strong>Género:</strong> Tyrannosaurus</li>
    <li><strong>Especie:</strong> T. rex</li>
</ul>

<h2>Periodo geológico</h2>
<p>El Tyrannosaurus rex vivió durante el <strong>Cretácico Tardío</strong>, específicamente entre hace <strong>68 y 66 millones de años</strong>, en la etapa Maastrichtiense, justo antes de la gran extinción del Cretácico-Paleógeno (K-Pg).</p>

<h2>Distribución geográfica</h2>
<p>Habitó lo que hoy es América del Norte. Sus fósiles han sido encontrados principalmente en:
<ul>
    <li>Montana (Estados Unidos)</li>
    <li>Wyoming</li>
    <li>Dakota del Sur</li>
    <li>Alberta y Saskatchewan (Canadá)</li>
</ul>
</p>

<h2>Características físicas</h2>
<ul>
    <li><strong>Longitud:</strong> Hasta 13 metros</li>
    <li><strong>Altura:</strong> 4 metros a la cadera</li>
    <li><strong>Peso:</strong> Hasta 9 toneladas</li>
    <li><strong>Craneo:</strong> Hasta 1.5 metros de largo, con una mordida de fuerza estimada en más de 6 toneladas</li>
    <li><strong>Brazos:</strong> Extremadamente cortos, con dos dedos, pero musculosos</li>
    <li><strong>Dientes:</strong> Afilados, cónicos y reemplazables, con una longitud de hasta 30 cm</li>
    <li><strong>Cola:</strong> Larga y pesada, utilizada para equilibrar el cuerpo</li>
</ul>

<h2>Alimentación</h2>
<p>El T. rex era un <strong>carnívoro obligado</strong>. Cazaba grandes dinosaurios herbívoros como Triceratops y Edmontosaurus, aunque también se ha sugerido que practicaba el carroñerismo cuando tenía oportunidad. Tenía una capacidad olfativa muy desarrollada, lo que le habría ayudado a rastrear cadáveres a gran distancia.</p>

<h2>Comportamiento</h2>
<p>Durante años se debatió si era un cazador activo o un simple carroñero. Actualmente se considera que probablemente hacía ambas cosas. Tenía un excelente sentido del olfato, una visión binocular aguda y una gran inteligencia en comparación con otros dinosaurios carnívoros.</p>

<h2>Reproducción</h2>
<p>Como todos los dinosaurios, el T. rex se reproducía por medio de <strong>huevos</strong>. Las hembras probablemente depositaban los huevos en nidos construidos en el suelo. Se estima que las crías eran relativamente grandes al nacer y crecían rápidamente.</p>

<h2>Crecimiento y desarrollo</h2>
<p>Estudios sugieren que el T. rex tenía un crecimiento acelerado en la adolescencia, con un aumento de peso de hasta 800 kg por año. Al llegar a la adultez, este crecimiento se detenía. Se estima que alcanzaba la madurez sexual alrededor de los 15 años.</p>

<h2>Esperanza de vida</h2>
<p>Se calcula que el T. rex vivía entre <strong>25 y 30 años</strong>. Uno de los fósiles más completos encontrados, apodado "Sue", tenía aproximadamente 28 años cuando murió.</p>

<h2>Descubrimiento</h2>
<p>El primer esqueleto parcial fue hallado en 1902 por Barnum Brown en Montana. El nombre oficial fue otorgado en 1905. Desde entonces, se han encontrado decenas de especímenes, incluido el famoso fósil "Sue", el T. rex más grande y completo descubierto hasta la fecha.</p>

<h2>Relación con otros dinosaurios</h2>
<p>El T. rex pertenecía a la familia Tyrannosauridae, y sus parientes cercanos incluyen a:
<ul>
    <li>Albertosaurus</li>
    <li>Daspletosaurus</li>
    <li>Tarbosaurus</li>
</ul>
Todos ellos comparten rasgos similares, como cráneos masivos y extremidades delanteras reducidas.
</p>

<h2>Extinción</h2>
<p>El Tyrannosaurus rex desapareció hace unos 66 millones de años en el evento de extinción masiva causado por el impacto de un asteroide en la actual península de Yucatán. Este evento causó cambios climáticos extremos y el colapso de cadenas alimenticias terrestres y marinas.</p>

<h2>Importancia cultural</h2>
<p>Es uno de los dinosaurios más reconocidos y populares. Ha aparecido en numerosas películas, libros, documentales y videojuegos. Su figura es central en franquicias como <em>Jurassic Park</em> y representa la imagen clásica de un dinosaurio depredador por excelencia.</p>

<h2>Curiosidades</h2>
<ul>
    <li>Podía correr a velocidades de hasta 27 km/h, aunque su tamaño le impedía giros rápidos.</li>
    <li>Los estudios de su oído interno indican que podía escuchar frecuencias bajas, útiles para rastrear presas o detectar amenazas.</li>
    <li>Tenía uno de los cerebros más grandes en proporción al cuerpo de todos los dinosaurios terópodos.</li>
    <li>Los fósiles de crías muestran que los juveniles eran más esbeltos y ágiles que los adultos.</li>
    <li>Sus dientes podían triturar huesos, una característica inusual entre dinosaurios carnívoros.</li>
</ul>

