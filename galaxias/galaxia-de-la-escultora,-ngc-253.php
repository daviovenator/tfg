        <h2>Historia de NGC 253</h2>
        <p>NGC 253 fue descubierta por el astrónomo alemán William Herschel en 1783. Desde su descubrimiento, ha sido objeto de estudio por parte de astrónomos debido a su gran brillo y actividad de formación estelar. En el siglo XIX, se identificó como una galaxia espiral, aunque en ese momento no se comprendía completamente la estructura de las galaxias y su naturaleza.</p>
        <p>En los últimos años, las observaciones en diferentes longitudes de onda, como el infrarrojo y el ultravioleta, han permitido a los astrónomos estudiar la formación estelar y la dinámica interna de NGC 253 con mayor detalle. Es uno de los mejores ejemplos de galaxias activas cercanas y un laboratorio ideal para investigar la formación estelar y la actividad galáctica.</p>

