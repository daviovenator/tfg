                <h3>Estructura de NGC 253</h3>
                <p>La estructura de NGC 253 se puede dividir en varias partes clave:</p>
                <ul>
                    <li><strong>El Núcleo:</strong> Es un núcleo brillante y activo que contiene un agujero negro supermasivo y muchas estrellas viejas. Se cree que el agujero negro de NGC 253 tiene una masa equivalente a millones de veces la de nuestro Sol.</li>
                    <li><strong>El Disco Espiral:</strong> NGC 253 tiene un disco espiral bien definido, con brazos llenos de gas y polvo, donde se forman muchas estrellas jóvenes. La densidad de estrellas jóvenes y de formación estelar es considerable en la galaxia.</li>
                    <li><strong>El Halo Estelar:</strong> Alrededor del disco de la galaxia se encuentra un halo estelar que contiene estrellas más viejas y cúmulos globulares.</li>
