        <h2>¿Qué es NGC 253?</h2>
        <p>NGC 253, también conocida como la Galaxia de la Escultora, es una galaxia espiral barrada situada en el Cúmulo de la Escultora, aproximadamente a 11 millones de años luz de la Tierra. Es una de las galaxias más cercanas a nuestro vecindario galáctico y una de las más brillantes del Cúmulo de la Escultora.</p>
        <p>Esta galaxia tiene un diámetro de aproximadamente 70,000 años luz y contiene una gran cantidad de estrellas jóvenes, especialmente en su disco central, lo que la convierte en una galaxia activa y altamente productiva en cuanto a formación estelar.</p>
