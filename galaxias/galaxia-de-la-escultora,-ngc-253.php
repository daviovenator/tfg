                <h3>Composición de NGC 253</h3>
                <p>NGC 253 es una galaxia espiral barrada, lo que significa que tiene un núcleo alargado en forma de barra, rodeado por un disco en espiral. Este disco está lleno de gas, polvo y una gran cantidad de estrellas. La galaxia es muy activa en términos de formación estelar, lo que se refleja en su alto nivel de emisión de luz en longitudes de onda ultravioletas.</p>
                <p>Una característica destacada de NGC 253 es su núcleo altamente energético, que alberga un agujero negro supermasivo. Este agujero negro está alimentado por la materia que cae hacia él desde las regiones cercanas del núcleo galáctico.</p>
