        <h2>Datos Curiosos de NGC 253</h2>
        <p>Algunos datos fascinantes sobre NGC 253:</p>
        <ul>
            <li>El agujero negro supermasivo en el centro de NGC 253 tiene una masa de aproximadamente 10 millones de veces la masa del Sol.</li>
            <li>NGC 253 está a unos 11 millones de años luz de la Tierra, lo que la convierte en una de las galaxias más cercanas y brillantes del Cúmulo de la Escultora.</li>
            <li>La galaxia está muy activa en la formación de estrellas, especialmente en sus brazos espirales.</li>
            <li>NGC 253 es una de las galaxias más observadas por los astrónomos debido a su proximidad y alta actividad.</li>
        </ul>

        <h2>El Futuro de NGC 253</h2>
        <p>Se espera que NGC 253 continúe siendo una galaxia activa en términos de formación estelar y emisiones de radiación. Su alto nivel de actividad y la presencia de un agujero negro supermasivo en su núcleo sugieren que seguirá siendo un objeto de gran interés para los astrónomos. En el futuro, se podría estudiar más a fondo el comportamiento de su agujero negro y cómo la interacción de la materia en su núcleo afecta a la galaxia en su conjunto.</p>
        <p>Sin embargo, NGC 253 eventualmente se verá afectada por la interacción con otras galaxias cercanas en el Cúmulo de la Escultora, aunque esto ocurrirá en un futuro distante.</p>

