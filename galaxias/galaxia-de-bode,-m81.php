                <h3>Interacción con la Galaxia M82</h3>
                <p>La Galaxia M81 está en una interacción gravitacional con la galaxia M82, conocida como la Galaxia Cigarro, que está situada justo al norte de M81. Esta interacción ha tenido un gran impacto en la estructura de M82, dándole su forma alargada y altamente activa, pero no ha causado una gran distorsión en la estructura de M81. Sin embargo, se cree que la interacción de M81 con M82 podría haber estimulado la formación de estrellas en ambas galaxias.</p>
                <p>La proximidad de M81 y M82 también hace que sean un ejemplo clásico de cómo las galaxias cercanas pueden influirse mutuamente, alterando la formación de estrellas y el comportamiento de los agujeros negros supermasivos en sus núcleos.</p>
