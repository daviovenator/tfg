        <h2>Historia del Descubrimiento</h2>
        <p>La Galaxia M81 fue descubierta por el astrónomo alemán Johann Elert Bode en 1774, y desde entonces ha sido un objeto importante en la investigación astronómica. Bode la incluyó en su catálogo como el objeto número 81 (de ahí su nombre). Durante el siglo XIX, los astrónomos comenzaron a estudiar la estructura de M81, y se identificó como una galaxia espiral, similar a la Vía Láctea.</p>
        <p>En los últimos años, M81 ha sido estudiada en profundidad utilizando telescopios avanzados como el Hubble, y se han observado en ella fenómenos como la formación de nuevas estrellas, la actividad del agujero negro supermasivo y la interacción gravitacional con M82.</p>

        <h2>Datos Curiosos sobre la Galaxia M81</h2>
        <ul>
            <li>La Galaxia M81 está a aproximadamente 12 millones de años luz de la Tierra, lo que la convierte en una de las galaxias más cercanas a nuestro grupo local.</li>
            <li>Se cree que M81 tiene una masa de aproximadamente 100 mil millones de veces la masa del Sol.</li>
            <li>El agujero negro supermasivo en el centro de M81 tiene una masa estimada en unos 70 millones de masas solares.</li>
            <li>M81 forma parte del Grupo de Galaxias de la Osa Mayor, que incluye otras galaxias como M82 (la Galaxia Cigarro), NGC 3077 y NGC 2976.</li>
            <li>La galaxia es una de las más brillantes en el cielo y es visible con telescopios de aficionados pequeños.</li>
        </ul>

