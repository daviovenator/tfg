        <h2>El Futuro de la Galaxia M81</h2>
        <p>En el futuro, se espera que M81 continúe interactuando con M82 y otras galaxias en su vecindad, lo que podría seguir influyendo en su estructura y en la formación de nuevas estrellas. Aunque M81 se encuentra a una distancia considerable de la Vía Láctea, las interacciones gravitacionales que ocurren dentro del Grupo de Galaxias de la Osa Mayor seguirán siendo un área activa de investigación astronómica.</p>
        <p>Es probable que M81 continúe siendo un objeto importante para los astrónomos que estudian la evolución de las galaxias espirales y las dinámicas de los agujeros negros supermasivos.</p>

