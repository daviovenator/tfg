                <h3>Características de la Galaxia M81</h3>
                <p>La Galaxia M81 es una galaxia espiral de tipo Sb, lo que significa que tiene un núcleo brillante rodeado por brazos espirales más abiertos. A diferencia de la Vía Láctea, que tiene un agujero negro supermasivo relativamente moderado, el agujero negro de M81 es mucho más masivo, con una estimación de alrededor de 70 millones de masas solares.</p>
                <p>Además de su brillante núcleo, M81 tiene varios brazos espirales bien definidos que contienen grandes regiones de formación estelar. La galaxia también tiene un halo de estrellas viejas y cúmulos globulares, lo que la convierte en una galaxia típica del tipo espiral barrada, aunque no tiene una barra prominente como algunas otras espirales.</p>
