        <h2>¿Qué es la Galaxia M81?</h2>
        <p>La Galaxia M81, también conocida como NGC 3031 o la Galaxia Bode, es una galaxia espiral ubicada a unos 12 millones de años luz de la Tierra, en la constelación de la Osa Mayor. Es una de las galaxias más grandes y luminosas del Grupo de Galaxias de la Osa Mayor, un grupo que incluye varias otras galaxias cercanas.</p>
        <p>La M81 es una galaxia espiral de gran tamaño, muy similar a nuestra propia Vía Láctea, y ha sido un objeto de estudio importante en la astronomía debido a su relativa proximidad y su brillantez. Se cree que M81 tiene una masa y estructura similar a la de la Vía Láctea, y su observación ha proporcionado valiosa información sobre las galaxias espirales en general.</p>

