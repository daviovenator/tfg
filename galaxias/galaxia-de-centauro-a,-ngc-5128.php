                <h3>Composición de NGC 5128</h3>
                <p>NGC 5128 tiene una estructura elíptica característica de una galaxia elíptica gigante. Sin embargo, a diferencia de muchas otras galaxias elípticas, presenta un gran disco de polvo oscuro que atraviesa su núcleo, lo que le da una forma distintiva. El núcleo de Centaurus A es brillante y activo, y contiene un agujero negro supermasivo que está alimentado por material de su entorno cercano.</p>
                <p>Este agujero negro es responsable de la intensa radiación de rayos X que emite la galaxia. Además, la galaxia está rodeada por un halo de gas caliente y cúmulos estelares que son visibles a través de telescopios avanzados.</p>
