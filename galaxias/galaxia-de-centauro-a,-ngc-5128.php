        <h2>Historia de NGC 5128</h2>
        <p>NGC 5128 fue descubierta por el astrónomo James Dunlop en 1826, pero fue observada y estudiada más en profundidad en el siglo XX. Durante mucho tiempo, los astrónomos no entendieron completamente la extraña forma de la galaxia hasta que comenzaron a investigar las colisiones galácticas, lo que llevó a la teoría de que Centaurus A fue el resultado de una colisión entre dos galaxias.</p>
        <p>En los últimos años, los telescopios como el Hubble han proporcionado imágenes detalladas de la galaxia, permitiendo a los astrónomos estudiar su estructura interna y la actividad en su núcleo.</p>

        <div class="highlight">
            <p>NGC 5128 se encuentra en una etapa activa de formación de estrellas, lo que la convierte en un objetivo de estudio clave en la investigación de galaxias activas y de formación estelar.</p>
