        <h2>Datos Curiosos de NGC 5128</h2>
        <p>Algunos datos interesantes sobre NGC 5128:</p>
        <ul>
            <li>El agujero negro supermasivo en el centro de NGC 5128 tiene una masa estimada de alrededor de 55 millones de veces la del Sol.</li>
            <li>La galaxia tiene un tamaño de aproximadamente 100,000 años luz de diámetro, lo que la hace comparable en tamaño a nuestra propia Vía Láctea.</li>
            <li>NGC 5128 es conocida por su forma peculiar, que es el resultado de una posible fusión entre una galaxia espiral y una elíptica.</li>
            <li>La galaxia emite una gran cantidad de radiación en el espectro de rayos X debido a la actividad del agujero negro supermasivo en su núcleo.</li>
        </ul>

        <h2>El Futuro de NGC 5128</h2>
        <p>En el futuro, se espera que NGC 5128 siga siendo una galaxia activa en términos de formación estelar y actividad nuclear. A medida que su agujero negro central sigue alimentándose de material cercano, la galaxia continuará siendo una fuente de radiación intensa, lo que la convierte en un objeto de gran interés para los astrónomos.</p>
        <p>Además, debido a su proximidad, NGC 5128 seguirá siendo un objeto de observación clave para estudiar cómo las galaxias evolucionan y cómo las interacciones y colisiones galácticas afectan a las galaxias a lo largo del tiempo.</p>

