        <h2>¿Qué es NGC 5128?</h2>
        <p>NGC 5128, conocida también como **Centaurus A**, es una galaxia elíptica gigante que se encuentra a unos 12 millones de años luz de la Tierra, en la constelación de Centauro. Esta galaxia es una de las más cercanas a nuestra Vía Láctea y es famosa por su forma peculiar y su activa región central.</p>
        <p>Centaurus A tiene una apariencia única, con un núcleo brillante rodeado por un halo de gas y polvo oscuro. La galaxia se ha convertido en un objeto de gran interés debido a su forma asimétrica, que se debe a la colisión con otra galaxia hace miles de millones de años.</p>
