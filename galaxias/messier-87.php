        <h2>Historia de Messier 87</h2>
        <p>Messier 87 fue descubierta por el astrónomo francés Charles Messier en 1781 y fue catalogada como el objeto Messier 87. Desde su descubrimiento, ha sido estudiada en profundidad debido a su enorme tamaño y la presencia de un agujero negro supermasivo en su centro.</p>
        <p>En 2019, M87 adquirió una notoriedad aún mayor cuando el telescopio Event Horizon Telescope (EHT) logró capturar la primera imagen de un agujero negro. La imagen mostró una especie de "sombra" en el centro de la galaxia, rodeada por un brillante anillo de luz, lo que confirmaba la existencia del agujero negro de M87 y abría nuevas puertas en la astronomía moderna.</p>

        <div class="highlight">
            <p>Messier 87 fue la primera galaxia en la que se logró capturar una imagen directa de un agujero negro, lo que marcó un hito en la historia de la astronomía.</p>
