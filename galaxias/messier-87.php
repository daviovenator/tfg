        <h2>¿Qué es Messier 87?</h2>
        <p>Messier 87 (M87) es una galaxia elíptica gigante ubicada en el cúmulo de Virgo, a unos 53 millones de años luz de la Tierra. Es una de las galaxias más masivas que conocemos y contiene un agujero negro supermasivo en su centro, que se convirtió en famoso en 2019 cuando el telescopio Event Horizon Telescope (EHT) capturó la primera imagen directa de su horizonte de eventos.</p>
        <p>M87 es una galaxia elíptica gigante, lo que significa que tiene una forma más redondeada y carece de los brazos espirales típicos de las galaxias espirales como la Vía Láctea. Esta galaxia es una de las más grandes del Grupo Local y una de las más brillantes del cúmulo de Virgo.</p>

