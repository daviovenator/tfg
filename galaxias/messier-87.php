                <p>Uno de los aspectos más fascinantes de Messier 87 es su agujero negro supermasivo, conocido como M87*. Este agujero negro tiene una masa equivalente a 6.5 mil millones de veces la del Sol, y fue el primero en ser fotografiado por el telescopio Event Horizon Telescope en 2019. La imagen mostró una estructura en forma de anillo brillante rodeando un oscuro centro, lo que confirma la existencia del agujero negro.</p>
