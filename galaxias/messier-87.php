        <h2>El Futuro de Messier 87</h2>
        <p>Messier 87 es una galaxia estable, pero como todas las galaxias, está en constante cambio. El agujero negro supermasivo en su centro continuará acumulando material, lo que generará enormes cantidades de energía en forma de radiación y chorros relativistas. Con el tiempo, es probable que la galaxia continúe evolucionando y transformándose.</p>
        <p>En cuanto a su interacción con otras galaxias, se espera que M87 continúe formando parte del cúmulo de Virgo y eventualmente se fusione con otras galaxias cercanas, lo que podría cambiar su estructura y características.</p>

